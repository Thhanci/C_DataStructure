/*
 * ============================================================
 * 希尔排序（Shell Sort）完整笔记
 * ============================================================
 * 
 * 核心思想：插入排序的改进版
 *   先按间隔 gap 分组排序（粗调），最后用插入排序收尾（精调）
 * 
 * 为什么更快？
 *   插入排序每次只移1步，希尔排序先大步跳着排，让数据快速到位
 * 
 * ============================================================
 * 一、执行过程
 * ============================================================
 * 
 * gap 从 n/2 开始，每次减半，直到 0
 * 最后一轮 gap=1 时，就是普通插入排序
 * 
 * 示例：n=7
 *   gap = 7/2 = 3  → 第1轮：间隔3分组排序
 *   gap = 3/2 = 1  → 第2轮：间隔1分组排序 = 插入排序
 *   gap = 1/2 = 0  → 停止
 * 
 * ============================================================
 * 二、代码结构
 * ============================================================
 * 
 * for (gap = n/2; gap > 0; gap /= 2) {
 *     // 对每个分组进行插入排序
 *     for (i = gap; i < n; i++) {
 *         temp = arr[i];
 *         // 同组内往前比较，大的往后移
 *         for (j = i; j >= gap && arr[j-gap] > temp; j -= gap) {
 *             arr[j] = arr[j-gap];
 *         }
 *         arr[j] = temp;
 *     }
 * }
 * 
 * ============================================================
 * 三、关键点
 * ============================================================
 * 
 * 1. gap 的含义：间隔，也是分组数
 *    - gap=3 时，分成 3 组：索引 (0,3,6)、(1,4)、(2,5)
 *    - gap=1 时，分成 1 组：所有元素
 * 
 * 2. 为什么要最后一轮 gap=1？
 *    只有相邻元素有序，整个数组才完全有序
 * 
 * 3. 前面几轮的作用？
 *    先让数据"大致有序"，最后插入排序就快了
 * 
 * 4. 时间复杂度
 *    最坏 O(n²)，平均 O(n^1.3~1.5)
 * 
 * ============================================================
 * 四、运行示例
 * ============================================================
 * 
 * 原始：64 34 25 12 22 11 90
 * 
 * 第1轮 gap=3：
 *   组1(0,3,6)：64,12,90 → 12,64,90
 *   组2(1,4)：  34,22   → 22,34
 *   组3(2,5)：  25,11   → 11,25
 *   结果：12 22 11 64 34 25 90
 * 
 * 第2轮 gap=1（插入排序）：
 *   12 → 12 22 11 64 34 25 90
 *   22 → 12 22 11 64 34 25 90
 *   11 → 11 12 22 64 34 25 90
 *   64 → 11 12 22 64 34 25 90
 *   34 → 11 12 22 34 64 25 90
 *   25 → 11 12 22 25 34 64 90
 *   90 → 11 12 22 25 34 64 90
 * 
 * 最终：11 12 22 25 34 64 90
 * 
 * ============================================================
 * 五、生活类比
 * ============================================================
 * 
 * 整理文件柜：
 *   第1步：每隔3个位置取一份整理（粗整理）
 *   第2步：每隔1个位置取一份整理（精整理）
 * 
 * 效果：先让文件大致按位置放好，最后微调就快了
 * 
 * ============================================================
 */

