/*
 * ============================================================
 * 第三阶段：高级排序 - 选择排序
 * 学习目标：理解每轮选最小放到前面的思想
 * 时间复杂度：O(n²)，不稳定排序
 * ============================================================
 * 输入：无（代码内置测试数据）
 * 输出：每轮的选择过程
 */
#include <stdio.h>

void selectionSort(int arr[], int n) {
    for (int i = 0; i < n - 1; i++) {
        int minIdx = i;
        for (int j = i + 1; j < n; j++) {
            if (arr[j] < arr[minIdx]) {
                minIdx = j;
            }
        }

        if (minIdx != i) {
            int temp = arr[i];
            arr[i] = arr[minIdx];
            arr[minIdx] = temp;
            printf("  第 %d 轮：选最小值 %d 放到位置 %d\n", i + 1, arr[i], i);
        } else {
            printf("  第 %d 轮：位置 %d 已是最小值\n", i + 1, i);
        }
    }
}

int main() {
    printf("========== 选择排序演示 ==========\n\n");

    int arr[] = {64, 34, 25, 12, 22, 11, 90};
    int n = 7;

    printf("原始数组：");
    for (int i = 0; i < n; i++) printf("%d ", arr[i]);
    printf("\n\n");

    selectionSort(arr, n);

    printf("\n--- 排序结果 ---\n");
    printf("最终数组：");
    for (int i = 0; i < n; i++) printf("%d ", arr[i]);
    printf("\n");

    return 0;
}

/*

 * ============================================================
 * 数组作为函数参数的本质
 * ============================================================
 * 
 * 问题：为什么调用时传 arr（数组名），函数接收却是 int arr[]？
 * 
 * 答案：因为 C 语言中，数组作为参数时会"退化"成指针。
 * 
 * ============================================================
 * 一、核心概念
 * ============================================================
 * 
 * 1. 数组名 = 数组首元素的地址（指针）
 *    int arr[] = {1, 2, 3};
 *    arr 等价于 &arr[0]  → 都是地址
 * 
 * 2. 函数调用时，传递的是地址，不是整个数组
 *    selectionSort(arr, n);  // 传递的是 arr 的地址
 * 
 * 3. 函数接收时，int arr[] 实际上是 int* arr
 *    void selectionSort(int arr[], int n) { }
 *    // 编译器自动把 int arr[] 当作 int* arr
 * 
 * ============================================================
 * 二、三种等价写法
 * ============================================================
 * 
 * // 写法1：数组语法（最常用，容易理解）
 * void func(int arr[], int n) {
 *     arr[0] = 100;  // 修改原数组
 * }
 * 
 * // 写法2：指针语法（真实情况）
 * void func(int* arr, int n) {
 *     arr[0] = 100;  // 同样有效
 * }
 * 
 * // 写法3：指针语法 + 解引用
 * void func(int* arr, int n) {
 *     *arr = 100;        // 改第一个
 *     *(arr + 1) = 200;  // 改第二个
 * }
 * 
 * 以上三种写法完全等价！
 * 
 * ============================================================
 * 三、关键区别：sizeof 的结果不同
 * ============================================================
 * 
 * // 在 main() 中（真正的数组）
 * int main() {
 *     int arr[] = {1, 2, 3, 4, 5};
 *     sizeof(arr);  // 20（整个数组占 20 字节）
 * }
 * 
 * // 在函数参数中（退化为指针）
 * void func(int arr[]) {
 *     sizeof(arr);  // 8（指针占 8 字节，64位系统）
 * }
 * 
 * 这就是为什么需要额外传 n（数组长度）！
 * 
 * ============================================================
 * 四、为什么这样设计？
 * ============================================================
 * 
 * 原因：性能优化
 *   - 如果传递整个数组，需要复制所有数据
 *   - 数组可能很大（成千上万个元素）
 *   - 复制成本太高
 *   - 所以 C 语言只传地址（指针），效率高
 * 
 * ============================================================
 * 五、图解
 * ============================================================
 * 
 * main() 中：
 * ┌──────────────────────────────┐
 * │ int arr[] = {64, 34, 25...} │
 * │ arr 是地址：0x1000           │
 * └──────────────────────────────┘
 *           │
 *           │ 调用 selectionSort(arr, n)
 *           │ 传递地址 0x1000
 *           ▼
 * ┌──────────────────────────────┐
 * │ void selectionSort(int arr[])│
 * │ arr 接收地址：0x1000         │
 * │ arr[0] → 访问 0x1000 的数据  │
 * │ arr[1] → 访问 0x1004 的数据  │
 * └──────────────────────────────┘
 * 
 * ============================================================
 * 六、常见误区
 * ============================================================
 * 
 * // ❌ 错误：认为传递的是整个数组
 * void func(int arr[]) {
 *     int len = sizeof(arr) / sizeof(arr[0]);  // ❌ 错误！
 *     // sizeof(arr) = 8（指针），不是数组大小
 * }
 * 
 * // ✅ 正确：需要额外传长度
 * void func(int arr[], int n) {
 *     for (int i = 0; i < n; i++) {  // 用 n
 *         // 处理 arr[i]
 *     }
 * }
 * 
 * // ✅ 正确：在 main() 中计算长度
 * int main() {
 *     int arr[] = {1, 2, 3, 4, 5};
 *     int n = sizeof(arr) / sizeof(arr[0]);  // ✅ 正确
 *     func(arr, n);
 * }
 * 
 * ============================================================
 * 七、总结
 * ============================================================
 * 
 * 数组传参三件事：
 *   1. 传的是地址，不是数组副本
 *   2. 形参 int arr[] 等于 int* arr
 *   3. 形参里 sizeof(arr) = 指针大小（8）
 * 
 * 一句话：int arr[] 是"伪数组"，实际是指针！
 *         所以传数组名（地址）和接收 int arr[] 正好匹配。
 * 
 * ============================================================
 * 
 */