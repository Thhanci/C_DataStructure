/*

    合并 [3, 27] 和 [38, 43]：

    第1步：3 vs 38 → 取3
    arr: [3, _, _, _, _, _, _]
        ↑
        k=1

    第2步：27 vs 38 → 取27
    arr: [3, 27, _, _, _, _, _]
            ↑
            k=2

    主循环结束！左边已空，右边还有 [38, 43]

    收尾：把右边的 [38, 43] 全部复制过去
    arr: [3, 27, 38, 43, _, _, _]
                ↑  ↑
            右边剩余元素

*/

//mergeSort 负责"拆"，merge 负责"合"——这就是归并排序的"分治"思想。
void mergeSort(int arr[], int left, int right, int depth) {//删了 depth 完全不影响排序逻辑。,仅用于打印
    if (left < right) {// 至少有两个元素才拆  //如果 left == right（只有一个元素），说明不能再拆了
        int mid = left + (right - left) / 2;    //mid 是左半部分的最后一个元素索引
        printf("%*s拆分 [%d..%d] → [%d..%d] + [%d..%d]\n",
               depth * 2, "", left, right, left, mid, mid + 1, right);
/*
    "" 对应 %*s 中的 字符串，depth * 2 对应 宽度。
    %*s 的意思是：打印一个字符串，但指定最小宽度，不足部分用空格填充。
*/
        mergeSort(arr, left, mid, depth + 1);   // 拆左半
        mergeSort(arr, mid + 1, right, depth + 1);  // 拆右半
        merge(arr, left, mid, right, depth);        // 合并左右

    }
}
 
int main() {
    printf("========== 归并排序演示 ==========\n\n");

    int arr[] = {38, 27, 43, 3, 9, 82, 10};
    int n = 7;

    printf("原始数组：");
    for (int i = 0; i < n; i++) printf("%d ", arr[i]);
    printf("\n\n");

    printf("--- 分治过程 ---\n");
    mergeSort(arr, 0, n - 1, 0);  //depth = 0 表示递归的起始深度（根节点），用于控制打印时的缩进，方便观察递归的层级结构。

    printf("\n--- 排序结果 ---\n");
    printf("最终数组：");
    for (int i = 0; i < n; i++) printf("%d ", arr[i]);
    printf("\n");

    return 0;
}

