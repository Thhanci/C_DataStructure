/*
 * ============================================================
 * 第三阶段：高级排序 - 归并排序
 * 学习目标：理解分治思想（先分再合）
 * 时间复杂度：O(n log n)，稳定排序
 * ============================================================
 * 输入：无（代码内置测试数据）
 * 输出：分治和合并过程
 */
#include <stdio.h>
#include <stdlib.h>

void merge(int arr[], int left, int mid, int right, int depth) {
    int n1 = mid - left + 1;   //1 2 3 4 5    //1 2 3
    int n2 = right - mid;      //0 1 2 3 4    //4 5
    int* L = (int*)malloc(n1 * sizeof(int));
    int* R = (int*)malloc(n2 * sizeof(int));

    for (int i = 0; i < n1; i++) L[i] = arr[left + i];
    for (int j = 0; j < n2; j++) R[j] = arr[mid + 1 + j];

    printf("%*s合并 [%d..%d] 和 [%d..%d]\n", depth * 2, "",
           left, mid, mid + 1, right);

    int i = 0, j = 0, k = left;
    while (i < n1 && j < n2) {
        if (L[i] <= R[j]) arr[k++] = L[i++];     //归并，把小的放前面
        else arr[k++] = R[j++];                     
    }
    //实际上，两个收尾只会执行其中一个！ 因为只要有一边空了，另一边一定有剩余。
    //如果有一边先空了执行
    while (i < n1) arr[k++] = L[i++];       //收尾，排序完，把剩余的数据存回arr
    while (j < n2) arr[k++] = R[j++];

    free(L);
    free(R);
}
